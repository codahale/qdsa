use std::ops::Mul;

use subtle::Choice;

use crate::hazmat::Point;
use crate::scalar::Scalar;

pub struct Generator([[u64; 5]; 256]);

impl Generator {
    pub fn as_point(&self) -> Point {
        Point([9, 0, 0, 0, 0])
    }
}

impl Mul<&Scalar> for &Generator {
    type Output = Point;

    fn mul(self, rhs: &Scalar) -> Self::Output {
        let mut u_r1 = Point::ONE;
        let mut z_r1 = Point::ONE;
        let mut u_r2 = G_MINUS_S;
        let mut z_r2 = Point::ONE;

        const N: usize = 255;
        const Q: usize = 3;

        let mut swap = Choice::from(1);
        let k = rhs.as_bytes();
        for i in 0..(N - Q) {
            let mu_i = Point(self.0[i]);

            // timing attack countermeasure
            let bit = ((k[(i + Q) / 8] >> ((i + Q) % 8)) & 1).into();
            swap ^= bit;
            u_r1.swap(&mut u_r2, swap);
            z_r1.swap(&mut z_r2, swap);
            swap = bit;

            // addition
            let a = &u_r1 + &z_r1;
            let b = &u_r1 - &z_r1;
            let c = &mu_i * &b;
            let d = &a + &c;
            let d = d.square();
            let e = &a - &c;
            let e = e.square();

            u_r1 = &z_r2 * &d;
            z_r1 = &u_r2 * &e;
        }

        for _ in 0..Q {
            // doubling
            let a = &u_r1 + &z_r1;
            let a = a.square();
            let b = &u_r1 - &z_r1;
            let b = b.square();
            u_r1 = &a * &b;
            let a = &a - &b;
            z_r1 = a.mul121666();
            z_r1 = &z_r1 + &b;
            z_r1 = &z_r1 * &a;
        }

        z_r1 = z_r1.invert();
        &u_r1 * &z_r1
    }
}

